int put_nbr (int nbr)
{
    if (nbr > 9)
        put_nbr (nbr / 10);
    write (1, &"0123456789"[nbr % 10], 1);
}

int main (int argc, char *argv[])
{
    (void)argv;
    put_nbr(argc - 1);
    write (1, "\n", 1);
    return (0);
}